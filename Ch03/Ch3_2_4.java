public class Ch3_2_4 {
   // 主程式
   public static void main(String[] args) {
      // 常數宣告
      final double PI = 3.1415926;
      double area;        // 變數宣告
      double r = 15.0;
      area = PI * r * r;  // 計算面積
      // 顯示面積
      System.out.print("面積: ");
      System.out.println(area);
   }
}